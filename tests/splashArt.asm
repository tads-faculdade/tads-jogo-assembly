#
# Entradas:
# 	$4 - endereço do primeiro pixel (canto superior esquerdo)
# 	$5 - altura
# 	$6 - largura
#	$7 - cor (32 bits)
#
# Usa:
#   $8, $9, $10, $11
##########################################
desenharQuadrado:
    	add $8, $0, $4     # $8 = endereço linha atual
    	add $9, $0, $5     # $9 = contador de linhas (altura)

forQuadrado:
    	beq  $9, $0, endForQuadrado	# se altura = 0 -> fim

